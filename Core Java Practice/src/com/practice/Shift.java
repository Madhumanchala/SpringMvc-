package com.practice;

public class Shift {
	public static void main(String[] args) {
		
		System.out.println(10<<2);//10*2^2=10*4=40 left shift me * karega 
		System.out.println(10<<3);//10*2^3=10*6=60
		System.out.println(10>>2);//10*2^2=10/4=2
		System.out.println(20>>2);//20*2^2=20/4=5 right shift me / karega 
	}

}
